    private boolean checkMaTN(String maTN) {
        boolean kt = true;
        if (maTN.equals("") == true) {
            tb_maTN.setText("Mã tiện nghi không được bỏ trống!");
            return false;
        } else {
            String mau = "[A-Za-z0-9]{1,10}";
            if (maTN.matches(mau) == false) {
                tb_maTN.setText("Mã tiện nghi không hợp lệ!");
                return false;
            }
        }
        try {
            Connection kn = qlks.QLKS.layKetNoi();
            PreparedStatement ps = kn.prepareStatement("select * from TIENNGHI where MATN = '" + maTN + "'");
            ResultSet rs = ps.executeQuery();
            if (rs.next()) {
                kt = false;
            }
            rs.close();
            ps.close();
            kn.close();
        } catch (SQLException ex) {
            Logger.getLogger(QuanLyTienNghi.class.getName()).log(Level.SEVERE, null, ex);
        }
        tb_maTN.setText("");
        if (kt == false) {
            tb_maTN.setText("Mã tiện nghi đã tồn tại!");
        }
        return kt;
    }

    private boolean checkTenTienNghi(String tenTienNghi) {
        if (tenTienNghi.equals("") == true) {
            tb_tenTienNghi.setText("Tên tiện nghi không được để trống!");
            return false;
        } else {
            String mau = "^\\p{L}+[\\p{L}\\p{Z}\\p{P}]{1,20}";
            if (tenTienNghi.matches(mau) == false) {
                tb_tenTienNghi.setText("Tên tiện nghi không hợp lệ!");
                return false;
            }
        }
        tb_tenTienNghi.setText("");
        return true;
    }

    private void them(String maTN, String tenTienNghi) {
        Connection kn = qlks.QLKS.layKetNoi();
        try {
            PreparedStatement ps = kn.prepareStatement("INSERT INTO TIENNGHI VALUES (?,?)");
            ps.setString(1, maTN);
            ps.setString(2, tenTienNghi);
            ps.executeUpdate();
        } catch (SQLException ex) {
            Logger.getLogger(QuanLyTienNghi.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    private void themActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_themActionPerformed
        // TODO add your handling code here:
        Boolean kt = true;
        String maTN = txt_maTN.getText();
        String tenTienNghi = txt_tenTienNghi.getText();
        if (checkMaTN(maTN) == false) {
            kt = false;
        }
        if (checkTenTienNghi(tenTienNghi) == false) {
            kt = false;
        }
        if (kt == true) {
            them(maTN, tenTienNghi);
            layDS();
            khoiTaoTXT();
            khoiTaoTB();
            JOptionPane.showMessageDialog(this, "Thêm tiện nghi thành công!");
        }
    }//GEN-LAST:event_themActionPerformed
