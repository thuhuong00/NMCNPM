    private void layDS() {
        DefaultTableModel dtm = (DefaultTableModel) table_DSTN.getModel();
        dtm.setNumRows(0);
        Connection kn = qlks.QLKS.layKetNoi();
        String sql = "select * from TIENNGHI";
        Vector vt;
        ds = new Vector();
        try {
            PreparedStatement ps = kn.prepareStatement(sql);
            ResultSet rs = ps.executeQuery();
            while (rs.next()) {
                vt = new Vector();
                vt.add(rs.getString("MATN"));
                vt.add(rs.getString("TENTIENNGHI"));
                ds.add(vt);
                dtm.addRow(vt);
            }
            table_DSTN.setModel(dtm);
            rs.close();
            ps.close();
            kn.close();
        } catch (SQLException ex) {
            Logger.getLogger(QuanLyTienNghi.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

